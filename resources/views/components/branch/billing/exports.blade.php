                console.log('DataTables blocked on exports page');
                return this;
            };
            $.fn.dataTable = function() {
                console.log('DataTables blocked on exports page');
                return this;
            };

            // Block any DataTables extensions
            $.fn.dataTable.ext = {
                errMode: 'none',
                type: {},
                search: {},
                order: {},
                feature: {}
            };
        }

        // Prevent any DataTables initialization
        $(document).ready(function() {
            // Remove any display classes that might trigger DataTables
            $('table').removeClass('display');
            $('table').removeClass('dataTable');

            // Block any DataTables scripts from loading
            $('script').each(function() {
                if ($(this).attr('src') && $(this).attr('src').includes('datatables')) {
                    $(this).remove();
                }
            });

            // Override any existing DataTables
            if ($.fn.DataTable) {
                $('.dataTable').each(function() {
                    try {
                        if ($(this).hasClass('dataTable')) {
                            $(this).removeClass('dataTable');
                        }
                    } catch (e) {
                        console.log('DataTables cleanup completed');
                    }
                });
            }
        });

        // Block DataTables errors globally
        $(document).on('error.dt', function(e) {
            e.preventDefault();
            e.stopPropagation();
            return false;
        });

        // Block any DataTables related errors
        window.addEventListener('error', function(e) {
            if (e.message && e.message.includes('DataTable')) {
                e.preventDefault();
                return false;
            }
        });

        // Override console.error to block DataTables errors
        var originalConsoleError = console.error;
        console.error = function() {
